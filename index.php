<p>
<h3>I. Registrácia (poslucháreň C):</h3>
<ul>
<li>osoby doprevádzajúce uchádzačov sa nesmú pohybovať v priestoroch vyhradených pre prijímacie pohovory;</li>
<li>uchádzač sa preukáže platným občianskym preukazom (cudzinec pasom);</li>
<li>uchádzač predloží notárom overenú kópiu maturitného vysvedčenia (ak ju ešte nepredložil), notárom overené kópie vysvedčení – z III. a IV. ročníka (ak ich ešte nepredložil);</li>
<li>uchádzač si vylosuje náramok s prezentačným číslom;</li>
<li>službukonajúci personál pripevní uchádzačovi náramok na ruku;</li>
<li>uchádzač je povinný si náramok ponechať na ruke počas prítomnosti v budove;</li>
<li>uvedené prezentačné číslo bude pre uchádzača slúžiť ako prístupový kód k výsledkom, ktoré budú zverejnené na webovej stránke podľa vnútorného predpisu Univerzity Komenského č. 6/2011;</li>
<li>poškodenie náramku môže byť dôvodom k vylúčeniu z prijímacej skúšky;</li>
<li>registračný systém je fyzicky oddelený od testovacieho systému.</li>
</ul>
</p>
<p>
<h3>II. Presun do počítačovej učebne (miestnosť č. H3):</h3>
<ul>
<li>uchádzači sa spolu so službukonajúcim personálom presunú do počítačovej miestnosti, v ktorej sa uskutoční prijímacie konanie;</li>
<li>počítačové miestnosti budú monitorované kamerovým bezpečnostným systémom.</li>
</ul>
</p>
<p>
<h3>III. Poučenie prodekana pred vykonaním prijímacej skúšky</h3>
<ul>
<li>uchádzači sú povinní vypnúť a uschovať všetky informačno-komunikačné zariadenia (napr. mobilné telefóny, fotoaparáty a pod.);</li>
<li>uchádzači sú povinní položiť na zem alebo uschovať do tašky všetky knihy a veci; jedinou povolenou pomôckou sú písacie potreby a to preto, lebo pri riešení úloh zameraných na logické usudzovanie si uchádzači budú môcť robiť poznámky;</li>
<li>prodekan prednesie konkrétnejšie  technicko-organizačné pokyny týkajúce sa priebehu prijímacej skúšky.</li>
</ul>
</p>
<p>
<h3>IV. Prijímacia skúška</h3>
<ul>
<li>čistý čas na vypracovanie skúšobného testu bude 60 minút;</li>
<li>test obsahuje 39 otázok, z ktorých 20 je zameraných na logické usudzovanie a 19 je zameraných na overovanie vedomostí;</li>
<li>po správnom zadaní číselného kódu sa na monitoroch objaví 39 otázok; každá otázka má tri odpovede;</li>
<li>v každej otázke je nutné označiť správnu odpoveď (alebo správne odpovede) kliknutím do políčka „ÁNO“ a taktiež je nutné označiť aj každú nesprávnu odpoveď (alebo nesprávne odpovede) kliknutím do políčka „NIE“;</li>
<li>prvých 20 otázok (zameraných na logické usudzovanie) má len jednu správnu odpoveď (neplatí pre slovnú úlohu);</li>
<li>zvyšných 19 otázok (ktoré sú zamerané na kontrolu vedomostí) môžu mať aj viac správnych odpovedí, prípadne žiadnu správnu odpoveď;</li>
<li>uchádzač sa pri vypĺňaní testu riadi pokynmi, ktoré sa mu zobrazujú na monitore;</li>
<li>po označení odpovedí na otázky uchádzač ukončí test stlačením tlačidla „odovzdať test“;</li>
<li>v prípade vypršania časového limitu sa test automaticky ukončí a odošle na hodnotenie;</li>
</ul>
</p>
<p>
<h3>V. Presun do priestorov určených na podpis prijímacích testov</h3>
<ul>
<li>po odovzdaní testu program bude informovať uchádzača, aby sa dostavil do priestorov určených na podpis vytlačených testov;</li>
<li>uchádzač po dostavení sa do priestorov určených na podpis testov čaká na vyzvanie, aby po predložení dokladov totožnosti skontroloval a podpísal vytlačený test;</li>
<li>podpísaním každej strany testu sa skončí prijímacia skúška;</li>
<li>priebežné a finálne poradie uchádzačov bude zverejnené na webe podľa vnútorného predpisu Univerzity Komenského č. 6/2011.</li>
</ul>
</p>
